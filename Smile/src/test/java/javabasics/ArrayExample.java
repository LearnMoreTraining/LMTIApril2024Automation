package javabasics;

public class ArrayExample {

    public static void main(String[] args) {

        int [] a = {10,20,30} ; //0,1,2

        for (int k =0 ; k < a.length ;k++){

            System.out.println(a[k]);
        }

        String [] f = {"learn","more"};

        for (int r =0 ; r < f.length ; r++){

            System.out.println(f[r]);
        }

    }
}
